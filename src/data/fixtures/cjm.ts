import type { CjmMap, CjmFlowNode, CjmFlowEdge, CjmNodeData } from '../types';

const COL = 280;
const ROW = { stage: 0, touchpoint: 170, emotion: 340, pain: 510, opportunity: 680 };

function s(id: string, col: number, label: string, metric?: string): CjmFlowNode {
  const data: CjmNodeData = { label };
  if (metric !== undefined) data.metric = metric;
  return { id, type: 'stage', position: { x: col * COL, y: ROW.stage }, data };
}
function tp(id: string, col: number, label: string, channel?: string): CjmFlowNode {
  const data: CjmNodeData = { label };
  if (channel !== undefined) data.channel = channel;
  return { id, type: 'touchpoint', position: { x: col * COL, y: ROW.touchpoint }, data };
}
function em(id: string, col: number, label: string, sentiment: 'positive' | 'neutral' | 'negative'): CjmFlowNode {
  return { id, type: 'emotion', position: { x: col * COL, y: ROW.emotion }, data: { label, sentiment } };
}
function pain(id: string, col: number, label: string): CjmFlowNode {
  return { id, type: 'pain', position: { x: col * COL, y: ROW.pain }, data: { label } };
}
function opp(id: string, col: number, label: string): CjmFlowNode {
  return { id, type: 'opportunity', position: { x: col * COL, y: ROW.opportunity }, data: { label } };
}
function edges(prefix: string, count: number): CjmFlowEdge[] {
  return Array.from({ length: count - 1 }, (_, i) => ({
    id: `${prefix}-e${i + 1}`,
    source: `${prefix}-s${i + 1}`,
    target: `${prefix}-s${i + 2}`,
  }));
}

