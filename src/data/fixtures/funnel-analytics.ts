// Q2 2026: 1 апр (ср) — 30 июн (вт), 91 день

function makePoints(values: number[]): MetricPoint[] {
  return values.map((value, i) => {
    const d = new Date('2026-04-01');
    d.setDate(d.getDate() + i);
    return { date: d.toISOString().slice(0, 10), value };
  });
}

